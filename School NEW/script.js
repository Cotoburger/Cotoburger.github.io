function updateLesson(shift) {
    const now = new Date();
    const currentHour = now.getHours();
    const currentMinutes = now.getMinutes();

    let currentTimeString = String(currentHour).padStart(2, '0') + ":" + String(currentMinutes).padStart(2, '0');

    let currentLesson = null;
    for (let i = 0; i < schedule[shift].length; i++) {
        const lesson = schedule[shift][i];
        if (currentTimeString >= lesson.start && currentTimeString <= lesson.end) {
            currentLesson = lesson;
            break;
        }
    }

    return currentLesson;
}

function updateProgressBar(lesson, progressBarId) {
    const now = new Date();
    const currentHour = now.getHours();
    const currentMinutes = now.getMinutes();
    const currentTimeInMinutes = currentHour * 60 + currentMinutes;

    const startTime = parseInt(lesson.start.replace(":", ""));
    const endTime = parseInt(lesson.end.replace(":", ""));
    const lessonDuration = endTime - startTime;

    let progress = ((currentTimeInMinutes - startTime) / lessonDuration) * 100;
    progress = Math.max(0, Math.min(100, progress)); // Ограничиваем прогресс от 0 до 100%

    document.getElementById(progressBarId).style.width = progress + "%";
}


function displayTime() {
    const now = new Date();
    let hours = now.getHours();
    let minutes = now.getMinutes();
    hours = String(hours).padStart(2, '0');
    minutes = String(minutes).padStart(2, '0');
    return `${hours}:${minutes}`;
}

function updateUI() {
    const currentShift = 1; // Можно изменить для выбора смены
    const lesson1 = updateLesson(currentShift);
    const lesson2 = updateLesson(2);

    document.getElementById("lesson1-name").textContent = lesson1 ? lesson1.lesson : "Перерыв";
    document.getElementById("lesson1-time").textContent = displayTime();
    updateProgressBar(lesson1, "lesson1-progress");

    document.getElementById("lesson2-name").textContent = lesson2 ? lesson2.lesson : "Перерыв";
    document.getElementById("lesson2-time").textContent = displayTime();
    updateProgressBar(lesson2, "lesson2-progress");
}

// Обновляем UI каждую минуту
setInterval(updateUI, 60000); // 60000 миллисекунд = 1 минута

// Первоначальное обновление при загрузке страницы
